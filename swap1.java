import java.util.*;
import java.lang.*;
import java.io.*;
class swap
{  
 public static void main(String args[])
 {  
  int a,b,temp;  
  Scanner sc=new Scanner(System.in);
  a=sc.nextInt();
  b=sc.nextInt();
  temp=a;
  a=b;
  b=temp;
  System.out.println("a:"+a);
  System.out.println("b:"+b);    
 }  
}  
